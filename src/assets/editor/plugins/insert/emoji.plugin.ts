  /**
   * –í—ã–ø–æ–ª–Ω–∏—Ç—å –≤—Å—Ç–∞–≤–∫—É —ç–º–æ–¥–∑–∏
   */
  execute(editorElement: HTMLElement, options?: { emoji?: string }): boolean {
    try {
      if (options?.emoji) {
        // –í—Å—Ç–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏ –Ω–∞–ø—Ä—è–º—É—é
        this.insertEmoji(editorElement, options.emoji);
        return true;
      }

      // –ï—Å–ª–∏ –µ—Å—Ç—å –∫–æ–º–ø–æ–Ω–µ–Ω—Ç –ø–∏–∫–µ—Ä–∞, –æ–Ω –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–ª–∏–∫ —Å–∞–º
      // (–æ—Ç–∫—Ä—ã–≤–∞–µ—Ç—Å—è —á–µ—Ä–µ–∑ dropdown –≤ toolbar)
      return true;
    } catch (error) {
      console.error('[EmojiPlugin] Error executing plugin:', error);
      return false;
    }
  }

  /**
   * –í—Å—Ç–∞–≤–∏—Ç—å —ç–º–æ–¥–∑–∏ –≤ —Ä–µ–¥–∞–∫—Ç–æ—Ä
   */
  insertEmoji(editorElement: HTMLElement, emoji: string): void {
    try {
      const selection = window.getSelection();
      if (!selection || selection.rangeCount === 0) {
        return;
      }

      const range = selection.getRangeAt(0);

      // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≤—ã–¥–µ–ª–µ–Ω–∏–µ –≤–Ω—É—Ç—Ä–∏ —Ä–µ–¥–∞–∫—Ç–æ—Ä–∞
      if (!editorElement.contains(range.commonAncestorContainer)) {
        return;
      }

      // –°–æ–∑–¥–∞—ë–º —Ç–µ–∫—Å—Ç–æ–≤—ã–π —É–∑–µ–ª —Å —ç–º–æ–¥–∑–∏
      const emojiNode = document.createTextNode(emoji);

      // –í—Å—Ç–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏
      range.deleteContents();
      range.insertNode(emojiNode);

      // –ü–µ—Ä–µ–º–µ—â–∞–µ–º –∫—É—Ä—Å–æ—Ä –ø–æ—Å–ª–µ —ç–º–æ–¥–∑–∏
      range.setStartAfter(emojiNode);
      range.collapse(true);
      selection.removeAllRanges();
      selection.addRange(range);

      // –î–æ–±–∞–≤–ª—è–µ–º –≤ –Ω–µ–¥–∞–≤–Ω–∏–µ
      this.addToRecent(emoji);

      // –¢—Ä–∏–≥–≥–µ—Ä–∏–º —Å–æ–±—ã—Ç–∏–µ input –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è
      editorElement.dispatchEvent(new Event('input', { bubbles: true }));
    } catch (error) {
      console.error('[EmojiPlugin] Error inserting emoji:', error);
    }
  }

