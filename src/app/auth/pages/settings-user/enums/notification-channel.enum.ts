/**
 * Каналы доставки уведомлений (Flags enum)
 * Можно комбинировать несколько каналов через побитовое ИЛИ
 */
export enum NotificationChannel {
  /** Нет уведомлений */
  None = 0,
  /** Email уведомления */
  Email = 1,
  /** Уведомления в приложении */
  InApp = 2,
  /** SMS уведомления */
  Sms = 4,
  /** Push уведомления */
  Push = 8,
}
